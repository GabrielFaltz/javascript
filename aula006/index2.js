//Não pode criar variáveis com palavras reservadas

// EXEMPLO: let console ou let if;

//Variáveis precisam ter nomes significativos

// EXEMPLO: let n = 'João' e não let n = 'CampoDeBaseDeDados'

// Não pode começar uma variável com um número

// EXEMPLO: let 1nome;

// Os nomes das variáveis não podem conter espaços ou traços

//Utiliza-se camelCase, oou seja, as palavras posteriores tem que ser em letra maiúscula.
